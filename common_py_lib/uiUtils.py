def lib_config_playbar():
    r"""
    Configure playbar options: Enable realtime playback.

    Args:
        None

    Returns:
        None
    """
    pb = hou.playbar
    # Enable only if realtime playback toggle is disabled.
    pb.setRealTime(True) if pb.isRealTime() == False else 0

